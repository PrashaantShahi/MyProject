//WAP that takes four integer and prints equal if all four are equal else not equal
package demo.java;

public class Equals {
		public void main(String args[])
		{
			int i=1,j=2,k=3,l=4;
		
			
			if(i==j && j==k && k==l)
			{
				System.out.println("Equal");
			}
			else
			{
				System.out.println("Not Equal");
			}
		
	}
}
