package demo.java;

public class SwapVariables {
	public static void main(String args[])
	{
		int a=1,b=2,temp=0;
		
		System.out.println("Before Swap");
		
		System.out.println("a="+a);
		
		System.out.println("b="+b);
		
		temp=a;
		
		a=b;
		
		b=temp;
		
		System.out.println("After Swap");
		
		System.out.println("a="+a);
		
		System.out.println("b="+b);
		
		}

}
